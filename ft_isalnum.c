#include "libft.h"

int	ft_isalnum(int n)
{
	if ((n >= '0' && n <= '9') || (n >= 'a' && n <= 'z')
		|| (n >= 'A' && n <= 'Z'))
	{
		return (8);
	}
	return (0);
}
